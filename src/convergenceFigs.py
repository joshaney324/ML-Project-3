def extract_mse(text):
    mse_values = []
    for line in text.split('\n'):
        if line.startswith('Training mse:'):
            try:
                mse = float(line.split(': ')[1])
                mse_values.append(mse)
            except:
                continue
        if line.startswith('Training Accuracy:'):
            try:
                mse = float(line.split(': ')[1])
                mse_values.append(mse)
            except:
                continue
    return mse_values


